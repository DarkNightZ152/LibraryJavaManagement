    private void hienthi(){
        try {
            phieumuontrai.removeAll();
            String [] arr = {"Mã phiếu mượn ","Mã độc giả","Tên độc giả ","Tên Sách ","Số lượng ","Ngày mượn","Số ngày mượn", "Ghi chú","Trạng Thái "};
            DefaultTableModel model;
            model = new DefaultTableModel(arr, 0);

            Connection connection =DBconnection.geConnection();

            String query = "select PM.mapm,DG.madg , DG.hoten, S.tensach, PM.soluong, PM.ngaymuon, PM.songaymuon, PM.ghichu, PM.trangthai from DocGia DG inner join Phieumuon PM on DG.madg = PM.madg inner join Sach S on PM.mas = S.mas  ";
            Statement statement =  connection.createStatement();
            ResultSet rs = statement.executeQuery(query);
            while(rs.next()) {
                Vector vct = new Vector();
                vct.add(rs.getString("mapm"));
                vct.add(rs.getString("madg"));
                vct.add(rs.getString("hoten"));
                vct.add(rs.getString("tensach"));
                vct.add(rs.getString("soluong"));
                vct.add(rs.getString("ngaymuon"));
                vct.add(rs.getString("songaymuon"));
                vct.add(rs.getString("ghichu"));
                vct.add(rs.getString("trangthai"));
 
                model.addRow(vct);        
            }
            DBconnection.closeConnection(connection);
            phieumuontrai.setModel(model);
            
        } catch (Exception e) {
    }
}
