    private void phieumuontraiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_phieumuontraiMouseClicked
        try {
            
            DefaultTableModel model = (DefaultTableModel)phieumuontrai.getModel();
            int row = phieumuontrai.getSelectedRow();
      
            String  mapm = phieumuontrai.getValueAt(row, 0).toString();
            String madg = phieumuontrai.getValueAt(row, 1).toString();
            String tensach = phieumuontrai.getValueAt(row, 3).toString();
            String  soluong = phieumuontrai.getValueAt(row, 4).toString();
            Date ngaymuon = (Date) new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(row, 5).toString());
            String  songaymuon = phieumuontrai.getValueAt(row, 6).toString();
            String  ghichu = phieumuontrai.getValueAt(row, 7).toString();
            String  trangthai = phieumuontrai.getValueAt(row, 8).toString();
            
            maphieumuon1.setText(mapm);
            madocgia1.setSelectedItem(madg);
            ngaymuon1.setDate(ngaymuon);
            songaymuon1.setText(songaymuon);
            ghichu1.setText(ghichu);
            trangthai1.setSelectedItem(trangthai);
            soluong1.setText(soluong);
            loadMaS(tensach);
            
        } catch (ParseException ex) {
            Logger.getLogger(phieumuon.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_phieumuontraiMouseClicked

